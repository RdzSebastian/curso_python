ahorros = int(input("Ingrese cantidad de ahorros: "))

primer_año = ahorros * 1.4
segundo_año = primer_año * 1.4
tercer_año = segundo_año * 1.4

print(f"Los ahorros luego del primer año seran: {primer_año:.2f}")
print(f"Los ahorros luego del segundo año seran: {segundo_año:.2f}")
print(f"Los ahorros luego del tercer año seran: {tercer_año:.2f}")
